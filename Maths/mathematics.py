def summation(a, b):
    '''
    Add function summation which takes in the a and b as a number arguments
    '''
    result = a + b
    return result

def subtraction(a, b):
    '''
    Add function subtraction which takes in the a and b as a number arguments
    '''
    result = a - b
    return result

def multiplication(a, b):
    '''
    Add function multiplication which takes in the a and b as a number arguments
    '''
    result = a * b
    return result
